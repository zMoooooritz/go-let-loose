func AllLoadouts() []Loadout {
	loadouts := []Loadout{}
	for _, l := range loadoutMap {
		loadouts = append(loadouts, l)
	}
	return loadouts
}

func LoadoutsByFaction(faction FactionIdentifier) []Loadout {
	loadouts := []Loadout{}
	for _, l := range loadoutMap {
		if l.Faction == faction {
			loadouts = append(loadouts, l)
		}
	}
	return loadouts
}

func LoadoutsByRole(role RoleIdentifier) []Loadout {
	loadouts := []Loadout{}
	for _, l := range loadoutMap {
		if l.Role == role {
			loadouts = append(loadouts, l)
		}
	}
	return loadouts
}
